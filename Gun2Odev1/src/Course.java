
public class Course {
	int id;
	String courseName;
	String instructorName;
	String detail;

	public Course() {
		
	}
	
	public Course(int id, String courseName, String instructorName, String detail) {
		this.id=id;
		this.courseName=courseName;
		this.instructorName=instructorName;
		this.detail=detail;
	}
	
	
}
